const state = {
  type: '告發',
  cases: [],
  stats: null,
  schema: null,
  selected: new Set(),
  passcode: localStorage.getItem('EEMS_PASSCODE') || ''
};

const $ = (id) => document.getElementById(id);

function headers() {
  return state.passcode ? { 'x-app-passcode': state.passcode } : {};
}

async function api(path, options = {}) {
  const opts = {
    ...options,
    headers: {
      ...(options.headers || {}),
      ...headers()
    }
  };
  if (opts.body && !(opts.body instanceof FormData) && !opts.headers['Content-Type']) {
    opts.headers['Content-Type'] = 'application/json';
  }
  const res = await fetch(path, opts);
  if (!res.ok) {
    let msg = res.statusText;
    try {
      const data = await res.json();
      msg = data.error || msg;
    } catch (_) {}
    throw new Error(msg);
  }
  const contentType = res.headers.get('content-type') || '';
  if (contentType.includes('application/json')) return res.json();
  return res;
}

function escapeHtml(value) {
  return String(value ?? '').replace(/[&<>"']/g, (ch) => ({
    '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#039;'
  }[ch]));
}

function badge(status) {
  const map = {
    '待初核': 'badge-pending',
    '待長官審核': 'badge-pending',
    '長官核可': 'badge-ok',
    '行政處理中': 'badge-admin',
    '退回修正': 'badge-reject',
    '已結案': 'badge-closed'
  };
  return `<span class="badge ${map[status] || 'badge-pending'}">${escapeHtml(status || '未分類')}</span>`;
}

function firstNonEmpty(...values) {
  return values.find((v) => String(v ?? '').trim()) || '';
}

function caseKey(item) {
  return item?.uid || item?.internalId || item?._uid || item?.id || '';
}

function isAutoGeneratedCaseNo(value) {
  return /^[AI]-[A-F0-9]{10}$/.test(String(value || '').trim());
}

function publicCaseNo(item) {
  const no = String(item?.id || item?.caseNo || item?.source?.['案件編號'] || '').trim();
  return isAutoGeneratedCaseNo(no) ? '' : no;
}

function displayCaseTitle(item) {
  return publicCaseNo(item) || item?.source?.['稽查單號'] || item?.source?.['稽查編號'] || item?.source?.['告發單號'] || caseKey(item);
}

function caseDate(item) {
  const s = item.source || {};
  return [s['量測日期'], s['稽查時間']].filter(Boolean).join(' ');
}

function caseLocation(item) {
  const s = item.source || {};
  return firstNonEmpty(s['量測位置地點'], `${s['便於複製(行政區)'] || ''}${s['便於複製(路段)'] || ''}`);
}

function dbText(item) {
  const s = item.source || {};
  const db = s['背景修正後分貝'] || '';
  const std = s['管制標準'] || '';
  return [db, std].filter(Boolean).join(' / ');
}

function repeatText(item) {
  const s = item.source || {};
  const direct = item.flags?.repeatOverLimit || item.flags?.escalated || s['是否累犯'] || s['1年內再次違反'] || s['前次告發'];
  if (!direct) return '';
  return item.flags?.repeatNote || '車號重複超標，註明加重';
}

function repeatBadge(item) {
  const text = repeatText(item);
  return text ? `<div class="case-meta"><span class="repeat-tag">加重</span> ${escapeHtml(text)}</div>` : '';
}

function editableSourceHeaders(type) {
  const all = state.schema?.[type]?.sourceHeaders || [];
  const priority = ['承辦複核','是否累犯','1年內再次違反','違規日期','前次告發','日數','車號','車種','量測日期','稽查時間','量測位置地點','便於複製(行政區)','便於複製(路段)','背景修正後分貝','管制標準','金額','稽查單號','告發單號','稽查編號','廠牌','出廠年月','車主姓名','證號','生日','郵遞區號','戶籍地','備註'];
  return priority.filter((h) => all.includes(h));
}

function progressText(item) {
  if (item.type === '通檢') {
    return firstNonEmpty(item.admin?.inspectionStatus, item.admin?.stage, '未啟動');
  }
  return firstNonEmpty(item.admin?.penaltyProgress, item.admin?.stage, '未裁處');
}

function updateKpi(stats) {
  const detail = stats?.byTypeDetail || {};
  const accusation = detail['告發'] || { total: 0, approved: 0, pendingReview: 0 };
  const inspection = detail['通檢'] || { total: 0, approved: 0, pendingReview: 0 };
  const kpiGrid = document.querySelector('.kpi-grid');
  kpiGrid.innerHTML = `
    <div class="kpi"><span>全部案件</span><strong>${escapeHtml(stats?.total ?? 0)}</strong><small>已審核 ${escapeHtml(stats?.approved ?? 0)} 件</small></div>
    <div class="kpi warn"><span>待長官審核</span><strong>${escapeHtml(stats?.pendingReview ?? 0)}</strong><small>全案待審</small></div>
    <div class="kpi ok"><span>告發案件</span><strong>${escapeHtml(accusation.total ?? 0)}</strong><small>已審核 ${escapeHtml(accusation.approved ?? 0)} 件｜待審 ${escapeHtml(accusation.pendingReview ?? 0)} 件</small></div>
    <div class="kpi info"><span>通檢案件</span><strong>${escapeHtml(inspection.total ?? 0)}</strong><small>已審核 ${escapeHtml(inspection.approved ?? 0)} 件｜待審 ${escapeHtml(inspection.pendingReview ?? 0)} 件</small></div>
    <div class="kpi done"><span>已結案</span><strong>${escapeHtml(stats?.closed ?? 0)}</strong><small>行政流程完成</small></div>
  `;
}

function renderStatusFilter() {
  const select = $('statusFilter');
  const current = select.value || '全部';
  select.innerHTML = '<option value="全部">全部狀態</option>';
  (state.schema?.statusOptions || []).forEach((s) => {
    const opt = document.createElement('option');
    opt.value = s;
    opt.textContent = s;
    select.appendChild(opt);
  });
  select.value = current;
}

function renderRows() {
  const tbody = $('caseTbody');
  if (!state.cases.length) {
    tbody.innerHTML = `<tr><td colspan="12" class="empty">目前沒有${escapeHtml(state.type)}案件。可先從 Google Sheet 同步。</td></tr>`;
    return;
  }

  tbody.innerHTML = state.cases.map((item) => {
    const s = item.source || {};
    const rowKey = caseKey(item);
    const checked = state.selected.has(rowKey) ? 'checked' : '';
    const approved = item.review?.chiefApproved ? 'checked' : '';
    const isClosed = item.status === '已結案';
    return `
      <tr>
        <td><input type="checkbox" class="row-select" data-id="${escapeHtml(rowKey)}" ${checked}></td>
        <td>${badge(item.status)}<div class="case-meta">${escapeHtml(item.admin?.stage || '')}</div></td>
        <td>
          <label class="check-approve">
            <input type="checkbox" class="chief-check" data-id="${escapeHtml(rowKey)}" ${approved} ${isClosed ? 'disabled' : ''}>
            核可
          </label>
          <div class="case-meta">${escapeHtml(item.review?.chiefReviewer || '')} ${escapeHtml(item.review?.chiefReviewedAt || '')}</div>
        </td>
        <td><strong>${escapeHtml(publicCaseNo(item) || '—')}</strong><div class="case-meta">${escapeHtml(s['稽查單號'] || s['稽查編號'] || s['告發單號'] || '')}</div></td>
        <td><strong>${escapeHtml(s['車號'] || '')}</strong>${repeatBadge(item)}</td>
        <td>${escapeHtml(item.type)}<div class="case-meta">${escapeHtml(s['車種'] || '')}</div></td>
        <td>${escapeHtml(caseDate(item))}</td>
        <td>${escapeHtml(caseLocation(item))}</td>
        <td><strong>${escapeHtml(dbText(item))}</strong></td>
        <td>${escapeHtml(item.admin?.officialDocumentNo || '')}</td>
        <td>
          <strong>${escapeHtml(progressText(item))}</strong>
          <div class="case-meta">${escapeHtml(item.admin?.deliveryDate || '')} ${escapeHtml(item.admin?.paymentStatus || '')}</div>
        </td>
        <td>
          <div class="row-actions">
            <button class="secondary-btn small-btn admin-btn" data-id="${escapeHtml(rowKey)}">行政追蹤</button>
            <button class="secondary-btn small-btn edit-btn" data-id="${escapeHtml(rowKey)}">修改</button>
            <button class="danger-btn small-btn reject-btn" data-id="${escapeHtml(rowKey)}">退回</button>
            <button class="danger-btn small-btn delete-btn" data-id="${escapeHtml(rowKey)}">刪除</button>
          </div>
        </td>
      </tr>
    `;
  }).join('');

  document.querySelectorAll('.row-select').forEach((el) => {
    el.addEventListener('change', (event) => {
      const id = event.currentTarget.dataset.id;
      if (event.currentTarget.checked) state.selected.add(id);
      else state.selected.delete(id);
    });
  });

  document.querySelectorAll('.chief-check').forEach((el) => {
    el.addEventListener('change', async (event) => {
      const id = event.currentTarget.dataset.id;
      if (event.currentTarget.checked) await reviewOne(id, true);
      else await reviewOne(id, false, '取消核可或退回補正');
    });
  });

  document.querySelectorAll('.admin-btn').forEach((el) => {
    el.addEventListener('click', () => openAdmin(el.dataset.id));
  });

  document.querySelectorAll('.reject-btn').forEach((el) => {
    el.addEventListener('click', async () => {
      const reason = prompt('請輸入退回修正原因：') || '資料需補正';
      await reviewOne(el.dataset.id, false, reason);
    });
  });

  document.querySelectorAll('.edit-btn').forEach((el) => {
    el.addEventListener('click', () => openEdit(el.dataset.id));
  });

  document.querySelectorAll('.delete-btn').forEach((el) => {
    el.addEventListener('click', () => deleteCase(el.dataset.id));
  });
}

async function loadSchema() {
  state.schema = await api('/api/schema');
  renderStatusFilter();
  fillSelect('stageInput', state.schema.adminStages);
  fillSelect('paymentStatusInput', state.schema.paymentStatusOptions);
  fillSelect('inspectionStatusInput', state.schema.inspectionStatusOptions);
}

async function loadCases() {
  const params = new URLSearchParams();
  params.set('type', state.type);
  const status = $('statusFilter').value;
  const q = $('searchInput').value.trim();
  if (status && status !== '全部') params.set('status', status);
  if (q) params.set('q', q);
  const data = await api(`/api/cases?${params.toString()}`);
  state.cases = data.items || [];
  state.stats = data.stats;
  updateKpi(data.stats);
  $('currentTypeLabel').textContent = state.type;
  renderRows();
}

function fillSelect(id, options) {
  const el = $(id);
  el.innerHTML = '';
  (options || []).forEach((value) => {
    const opt = document.createElement('option');
    opt.value = value;
    opt.textContent = value;
    el.appendChild(opt);
  });
}

async function healthCheck() {
  const box = $('healthBox');
  box.textContent = '連線測試中...';
  try {
    const data = await api('/api/health');
    box.innerHTML = `
      <strong>${escapeHtml(data.service)}</strong><br>
      系統狀態：正常<br>
      Google Sheet：${data.googleSheets.connected ? '連線成功' : '未連線'}<br>
      <span class="muted">${escapeHtml(data.googleSheets.message || '')}</span>
    `;
  } catch (err) {
    box.textContent = `連線失敗：${err.message}`;
  }
}

async function reviewOne(id, approved, reason = '') {
  const reviewer = prompt(approved ? '請輸入核可長官姓名：' : '請輸入退回人員姓名：', approved ? '局內長官' : '局內長官') || '局內長官';
  await api(`/api/cases/${encodeURIComponent(id)}/review`, {
    method: 'PATCH',
    body: JSON.stringify({ approved, reviewer, rejectedReason: reason })
  });
  await loadCases();
}

async function bulkReview(approved) {
  const ids = Array.from(state.selected);
  if (!ids.length) {
    alert('請先勾選案件。');
    return;
  }
  const reviewer = prompt(approved ? '請輸入核可長官姓名：' : '請輸入退回人員姓名：', '局內長官') || '局內長官';
  const reason = approved ? '' : (prompt('請輸入退回修正原因：') || '資料需補正');
  await api('/api/cases/bulk-review', {
    method: 'POST',
    body: JSON.stringify({ ids, approved, reviewer, rejectedReason: reason })
  });
  state.selected.clear();
  $('selectAll').checked = false;
  await loadCases();
}

async function importExcel(file) {
  if (!file) return;
  const form = new FormData();
  form.append('file', file);
  const data = await api(`/api/import/${encodeURIComponent(state.type)}`, {
    method: 'POST',
    body: form
  });
  alert(`匯入完成：新增 ${data.inserted} 筆、更新 ${data.updated} 筆。`);
  await loadCases();
}

function downloadExport(type) {
  const url = `/api/export/${encodeURIComponent(type)}${state.passcode ? `?passcode=${encodeURIComponent(state.passcode)}` : ''}`;
  window.location.href = url;
}

async function openEdit(id) {
  const item = await api(`/api/cases/${encodeURIComponent(id)}`);
  $('editOriginalId').value = caseKey(item);
  $('editCaseIdInput').value = publicCaseNo(item);
  $('editTypeInput').value = item.type;
  $('editStatusInput').innerHTML = (state.schema?.statusOptions || []).map((x) => `<option value="${escapeHtml(x)}">${escapeHtml(x)}</option>`).join('');
  $('editStatusInput').value = item.status || '待長官審核';
  $('editRepeatInput').checked = Boolean(item.flags?.repeatOverLimit || item.flags?.escalated);
  $('editRepeatNoteInput').value = item.flags?.repeatNote || (repeatText(item) ? '車號重複超標，註明加重' : '');
  $('editOperatorInput').value = localStorage.getItem('EEMS_OPERATOR') || '承辦人員';

  const headers = editableSourceHeaders(item.type);
  $('editSourceFields').innerHTML = headers.map((h) => `
    <label>${escapeHtml(h)}
      <input class="edit-source-input" data-key="${escapeHtml(h)}" value="${escapeHtml(item.source?.[h] || '')}" />
    </label>
  `).join('');

  $('editDialog').showModal();
}

async function saveEdit(event) {
  event.preventDefault();
  const id = $('editOriginalId').value;
  const source = {};
  document.querySelectorAll('.edit-source-input').forEach((el) => {
    source[el.dataset.key] = el.value;
  });
  const operator = $('editOperatorInput').value.trim() || '承辦人員';
  localStorage.setItem('EEMS_OPERATOR', operator);
  await api(`/api/cases/${encodeURIComponent(id)}/update`, {
    method: 'PATCH',
    body: JSON.stringify({
      id: $('editCaseIdInput').value.trim(),
      type: $('editTypeInput').value,
      status: $('editStatusInput').value,
      source,
      repeatOverLimit: $('editRepeatInput').checked,
      escalated: $('editRepeatInput').checked,
      repeatNote: $('editRepeatNoteInput').value,
      operator
    })
  });
  $('editDialog').close();
  await loadCases();
}

async function deleteCase(id) {
  if (!confirm(`確認刪除案件 ${id}？刪除後無法從系統復原。`)) return;
  const adminPassword = prompt('請輸入管理權限密碼：');
  if (!adminPassword) return;
  await api(`/api/cases/${encodeURIComponent(id)}`, {
    method: 'DELETE',
    headers: { 'x-admin-delete-passcode': adminPassword }
  });
  state.selected.delete(id);
  await loadCases();
}

async function openAdmin(id) {
  const item = await api(`/api/cases/${encodeURIComponent(id)}`);
  $('modalCaseId').value = caseKey(item);
  $('modalTitle').textContent = `行政流程追蹤｜${displayCaseTitle(item)}｜${item.source?.['車號'] || ''}`;

  $('stageInput').value = item.admin?.stage || '尚未啟動';
  $('officialDocumentNoInput').value = item.admin?.officialDocumentNo || '';
  $('officialDocumentDateInput').value = normalizeDate(item.admin?.officialDocumentDate);
  $('dispositionNoInput').value = item.admin?.dispositionNo || '';
  $('penaltyAmountInput').value = item.admin?.penaltyAmount || item.source?.['金額'] || '';
  $('deliveryDateInput').value = normalizeDate(item.admin?.deliveryDate || item.admin?.deliveryStatus);
  $('paymentStatusInput').value = item.admin?.paymentStatus || '未開立';
  $('inspectionDueDateInput').value = normalizeDate(item.admin?.inspectionDueDate);
  $('inspectionStatusInput').value = item.admin?.inspectionStatus || (item.type === '通檢' ? '通知待發文' : '未啟動');
  $('closeDateInput').value = normalizeDate(item.admin?.closeDate);
  $('notesInput').value = item.admin?.notes || '';

  const logs = item.logs || [];
  $('logList').innerHTML = logs.length ? logs.map((log) => `
    <div class="log-item">
      <span>${escapeHtml(log.at || '')}</span>
      <strong>${escapeHtml(log.action || '')}</strong>
      <span>${escapeHtml(log.by || '')}｜${escapeHtml(log.note || '')}</span>
    </div>
  `).join('') : '<div class="muted">尚無更新紀錄</div>';

  $('adminDialog').showModal();
}

function normalizeDate(value) {
  const v = String(value || '').trim();
  if (!v) return '';
  const m = v.match(/(\d{4})[-/](\d{1,2})[-/](\d{1,2})/);
  if (m) return `${m[1]}-${m[2].padStart(2, '0')}-${m[3].padStart(2, '0')}`;
  return '';
}

