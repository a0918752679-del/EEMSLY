function upsertCases(existing, incoming) {
  existing.forEach((item) => {
    if (!item.uid) item.uid = caseKey(item) || ('UID-' + crypto.randomUUID().replace(/-/g, '').slice(0, 16).toUpperCase());
    if (isAutoGeneratedCaseNo(item.id) && !item.source?.['案件編號']) item.id = '';
  });
  const map = new Map(existing.map((item) => [caseKey(item), item]));
  let inserted = 0;
  let updated = 0;
  incoming.forEach((item) => {
    if (!item.uid) item.uid = caseKey(item) || ('UID-' + crypto.randomUUID().replace(/-/g, '').slice(0, 16).toUpperCase());
    const key = caseKey(item);
    if (map.has(key)) {
      const old = map.get(key);
      const merged = {
        ...old,
        ...item,
        uid: old.uid || item.uid,
        logs: [...(item.logs || []), ...(old.logs || [])],
        createdAt: old.createdAt || item.createdAt,
        updatedAt: nowTaipei()
      };
      // Google Sheet 若案件編號空白，系統內也保留空白，不回填內部識別碼。
      merged.id = publicCaseNo(item);
      map.set(key, merged);
      updated += 1;
    } else {
      item.id = publicCaseNo(item);
      map.set(key, item);
      inserted += 1;
    }
  });
  return { cases: refreshRepeatFlags(Array.from(map.values())), inserted, updated };
}

function filterCases(cases, query) {
  let out = cases;
  if (query.type && query.type !== '全部') out = out.filter((c) => c.type === query.type);
  if (query.status && query.status !== '全部') out = out.filter((c) => c.status === query.status);
  if (query.q) {
    const q = String(query.q).toLowerCase();
    out = out.filter((c) => JSON.stringify(c).toLowerCase().includes(q));
  }
  return out;
}

