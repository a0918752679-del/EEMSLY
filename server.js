function addLog(item, action, by, note = '') {
  item.logs = Array.isArray(item.logs) ? item.logs : [];
  item.logs.unshift({ at: nowTaipei(), action, by: by || 'system', note });
  item.updatedAt = nowTaipei();
}

function normalizeText(value) {
  if (value === null || value === undefined) return '';
  if (value instanceof Date) return value.toISOString().slice(0, 10);
  return String(value).trim();
}

function normalizeHeaderName(value) {
  return normalizeText(value).replace(/\s+/g, '');
}

const HEADER_ALIASES = {
  '稽查編號': '稽查單號',
  '稽查單號': '稽查單號',
  '告發單號': '告發單號',
  '公文案號': '公文文號',
  '公文日期': '發文日期',
  '裁處書號': '裁處字號',
  '送達狀態': '送達日期'
};

function canonicalHeader(value) {
  const key = normalizeHeaderName(value);
  return HEADER_ALIASES[key] || key;
}

function canonicalizeRow(row = {}) {
  const out = {};
  Object.entries(row).forEach(([key, value]) => {
    const canonical = canonicalHeader(key);
    if (canonical) out[canonical] = value;
    const trimmed = normalizeText(key);
    if (trimmed && !Object.prototype.hasOwnProperty.call(out, trimmed)) out[trimmed] = value;
  });
  return out;
}

function firstNonEmpty(...values) {
  for (const value of values) {
    const v = normalizeText(value);
    if (v) return v;
  }
  return '';
}

function digitsOnly(value) {
  return normalizeText(value).replace(/\D/g, '');
}

function formatDispositionNo(value) {
  const d = digitsOnly(value);
  if (!d) return '';
  if (d.length === 11) return `${d.slice(0, 2)}-${d.slice(2, 5)}-${d.slice(5)}`;
  return normalizeText(value);
}

function getCaseIdentifier(type, row = {}) {
  const canonical = canonicalizeRow(row);
  if (type === '告發') {
    return firstNonEmpty(canonical['案件編號'], canonical['稽查單號'], canonical['告發單號']);
  }
  return firstNonEmpty(canonical['案件編號']);
}

function getSourceValue(source = {}, header) {
  const key = canonicalHeader(header);
  return firstNonEmpty(source[key], source[header], key === '稽查單號' ? source['稽查編號'] : '');
}

function hasMeaningfulCaseRow(type, row = {}) {
  const canonical = canonicalizeRow(row);
  return Boolean(
    firstNonEmpty(canonical['車號'], canonical['案件編號'], canonical['稽查單號'], canonical['告發單號']) ||
    (type === '通檢' && firstNonEmpty(canonical['車號'], canonical['量測日期'], canonical['量測位置地點']))
  );
}

function parseBool(value) {
  if (typeof value === 'boolean') return value;
  const v = normalizeText(value).toLowerCase();
  return ['true', '1', 'yes', 'y', '是', '核可', '已核可', 'v', '✓', '勾選'].includes(v);
}

function isRepeatSource(source = {}) {
  // 修正：前次告發僅作為人工參考，不再因欄位有文字就直接判斷加重。
  return parseBool(source['是否累犯']) || parseBool(source['1年內再次違反']);
}

function caseNeedsEscalation(item) {
  return Boolean(item?.flags?.repeatOverLimit || item?.flags?.escalated || isRepeatSource(item?.source));
}

function refreshRepeatFlags(cases) {
  const plateCounts = new Map();
  cases.forEach((item) => {
    const plate = normalizeText(item?.source?.['車號']).toUpperCase();
    if (plate) plateCounts.set(plate, (plateCounts.get(plate) || 0) + 1);
  });

  cases.forEach((item) => {
    const plate = normalizeText(item?.source?.['車號']).toUpperCase();
    const count = plate ? (plateCounts.get(plate) || 0) : 0;
    const sourceRepeat = isRepeatSource(item?.source);
    item.flags = item.flags || {};
    item.flags.plateRepeatCount = count;
    item.flags.repeatOverLimit = Boolean(plate && count > 1) || sourceRepeat;
    item.flags.escalated = item.flags.repeatOverLimit;
    item.flags.repeatNote = item.flags.repeatOverLimit
      ? `車號重複超標${count > 1 ? count + '件' : ''}，註明加重`
      : '';
  });
  return cases;
}

function isAutoGeneratedCaseNo(value) {
  return /^[AI]-[A-F0-9]{10}$/.test(normalizeText(value));
}

function makeInternalUid(type, row = {}, source = {}) {
  const canonical = canonicalizeRow(row);
  const seed = [
    type,
    canonical['案件編號'],
    canonical['稽查單號'],
    canonical['告發單號'],
    source['車號'],
    source['量測日期'],
    source['稽查時間'],
    source['量測位置地點']
  ].filter((v) => normalizeText(v)).join('|') || crypto.randomUUID();
  return 'UID-' + crypto.createHash('sha1').update(seed).digest('hex').slice(0, 16).toUpperCase();
}

function caseKey(item = {}) {
  return normalizeText(item.uid || item.internalId || item._uid || item.id);
}

function publicCaseNo(item = {}) {
  const no = normalizeText(item.id || item.caseNo || item.source?.['案件編號']);
  return isAutoGeneratedCaseNo(no) ? '' : no;
}

function displayCaseTitle(item = {}) {
  return publicCaseNo(item) || normalizeText(item.source?.['稽查單號'] || item.source?.['稽查編號'] || item.source?.['告發單號']) || caseKey(item);
}

function findCaseIndexById(cases, id) {
  const key = normalizeText(id);
  return cases.findIndex((c) => caseKey(c) === key || normalizeText(c.id) === key);
}

