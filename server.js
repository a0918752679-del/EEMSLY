function nowTaipei() {
  return new Date().toISOString();
}

function todayTaipeiDate() {
  const dtf = new Intl.DateTimeFormat('sv-SE', {
    timeZone: 'Asia/Taipei',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit'
  });
  return dtf.format(new Date());
}

